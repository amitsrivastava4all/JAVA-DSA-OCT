public strictfp class DataTypes {
    public static  void main(String[] args) {
        char w = 'A';
        char tt = 100;
        char h = 'рди';
        System.out.println(w);
        System.out.println(tt);
        System.out.println(h);
        boolean isLogin = true;
        int z = 100; // int literal
        float price = 100.20F; // float literal
        double p2 = 1000.43535674;
        float e = 10;
        System.out.println(e);
        //var a = 10;
        //int b = 20;
        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.MIN_VALUE);
        byte r = (byte)128;
        System.out.println(r);
        byte age = 21; // byte literal
        byte a = 10; 
        byte b = 20;
        byte c = (byte)(a + b); // Explicit TypeCasting
        int d = a + b; //int d = byte + byte (Implicit Type Casting)
        System.out.println(10/2);
        System.out.println(10%2);


    }
}
